def calcular_promedio_columnas(matriz):
    num_filas = len(matriz)
    num_columnas = len(matriz[0])
    
    promedios = [0] * num_columnas
    
    for columna in range(num_columnas):
        suma_columna = sum(matriz[fila][columna] for fila in range(num_filas))
        promedios[columna] = suma_columna / num_filas
    
    return promedios

